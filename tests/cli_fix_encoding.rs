use std::fs;
use std::process::Command;
use tempfile::tempdir;

fn run(cmd: &mut Command) -> (i32, String, String) {
    let out = cmd.output().expect("process");
    let code = out.status.code().unwrap_or(-1);
    let stdout = String::from_utf8_lossy(&out.stdout).into_owned();
    let stderr = String::from_utf8_lossy(&out.stderr).into_owned();
    (code, stdout, stderr)
}

fn write_config(dir: &std::path::Path) {
    fs::write(
        dir.join(".ryl.toml"),
        r#"[rules]
new-line-at-end-of-file = "enable"
document-start = "disable"
"#,
    )
    .unwrap();
}

fn assert_fix_round_trips_encoding(
    initial: &[u8],
    expected: &[u8],
    env_encoding: Option<&str>,
) {
    let dir = tempdir().unwrap();
    let file = dir.path().join("input.yaml");
    fs::write(&file, initial).unwrap();
    write_config(dir.path());

    let exe = env!("CARGO_BIN_EXE_ryl");
    let mut command = Command::new(exe);
    command.arg("--fix").arg(&file);
    if let Some(encoding) = env_encoding {
        command.env("YAMLLINT_FILE_ENCODING", encoding);
    }
    let (code, stdout, stderr) = run(&mut command);
    assert_eq!(
        code, 0,
        "fix should succeed: stdout={stdout} stderr={stderr}"
    );

    let raw = fs::read(&file).unwrap();
    assert_eq!(raw, expected);
}

